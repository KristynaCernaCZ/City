import cz.expertkom.ju.task.Nameable;

public class City implements Nameable{
	public String name;
	public int buildings;
	public int size;
	public int population;
	
	public City(String name, int buildings, int size, int population) {
		super();
		this.name = name;
		this.buildings = buildings;
		this.size = size;
		this.population = population;
	}
	
	
	

}
